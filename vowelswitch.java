public class vowelswitch{
    public static void main(String[] args) {
        char ch= 'e';
        switch (ch){
            case 'y':
                System.out.println("its not a vowl");
                break;
            case 'e' :   System.out.println("its a vowl");
            break;
            case 'o':   System.out.println("its not a vowl");
            break;
            default:   System.out.println("consonant");

        }


    }
}
