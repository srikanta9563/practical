public class switchtest {
    public static void main(String[] args) {
        int num = 10 ;
        switch (num){
            case 1 :
                System.out.println("12");
                break;
            case 12:   System.out.println("15");
            break;
            case 10:   System.out.println("very good");
            break;
            case 15:  System.out.println("20");
            break;
            default:
                System.out.println("number is not within");

        }


    }

}

